import psutil
import json
import datetime
import time

# 1. ??????? ? ???????????? ????
PORT_PROTOCOLS = {
    80: "HTTP",
    443: "HTTPS",
    21: "FTP",
    22: "SSH",
    23: "Telnet",
    3306: "MySQL",
    5432: "PostgreSQL"
}

# Firewall ???????? (???????????)
firewall_rules = {
    "blocked_in": {80, 23}, # ???????????? ????????? ??????
    "blocked_out": {21}     # ???????????? ??????? ??????
}

def scan_ports():
    print(f"--- ???????????: {datetime.datetime.now()} ---")
    active_connections = psutil.net_connections()
    results = []
    port_usage = {}

    for conn in active_connections:
        port = conn.laddr.port if conn.laddr else None
        if port:
            # ????????? ???????
            proto = PORT_PROTOCOLS.get(port, "TCP/UDP")
            
            # ????????? ????????
            status = "ALLOWED"
            if port in firewall_rules["blocked_in"]:
                status = "BLOCKED (Inbound)"
            elif port in firewall_rules["blocked_out"]:
                status = "BLOCKED (Outbound)"

            results.append({
                "port": port,
                "protocol": proto,
                "status": status,
                "local_address": f"{conn.laddr.ip}:{conn.laddr.port}",
                "state": conn.status
            })

            # ?????????????????? ???????
            port_usage[port] = port_usage.get(port, 0) + 1

    # ??????????? ??? ??????????????????
    busy_ports = sorted(port_usage.items(), key=lambda x: x[1], reverse=True)[:5]

    return results, busy_ports

def save_log(data, busy_ports):
    timestamp = datetime.datetime.now().strftime("%Y-%m-%d_%H-%M")
    filename = f"firewall_log_{timestamp}.json"
    
    log_data = {
        "timestamp": timestamp,
        "firewall_rules": {k: list(v) for k, v in firewall_rules.items()},
        "active_ports": data,
        "top_busy_ports": busy_ports
    }

    with open(filename, "w") as f:
        json.dump(log_data, f, indent=4)
    print(f"????????? ????????? ?? {filename} ???????:")

if __name__ == "__main__":
    data, busy = scan_ports()
    for entry in data[:10]: # ????????? ?????? 10-?
        print(f"Port: {entry['port']} | Proto: {entry['protocol']} | Status: {entry['status']}")
    
    print("\n???????? ??????????? ???????:", busy)
    save_log(data, busy)
